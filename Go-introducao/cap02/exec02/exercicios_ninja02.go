package main

import "fmt"

var x = 10

func main() {
	fmt.Println( x < 10)
	fmt.Println( x > 10)
	fmt.Println( x <= 10)
	fmt.Println( x >= 10)
	fmt.Println( x != 10)
	fmt.Println( x == 10)
	fmt.Println( x + 10 )
	fmt.Println( x - 10)
	fmt.Println( x * 10)
	fmt.Println( x / 10)
	fmt.Println( x % 10)
}

